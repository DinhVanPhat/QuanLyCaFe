    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblBan.getModel();
        model.setRowCount(0);
        try {
            String keyWord = txtTimKiem.getText();
            System.out.println(keyWord);
            List<Ban> list = banDao.selectByKeyWord(keyWord);
            for (Ban ban : list) {
                Object[] row = {ban.getMaBan(), ban.getTenBan(), ban.getTrangThai(), ban.getKhuVuc()};
                model.addRow(row);
            }

        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    void insert() {
        if (checkValidateForm()) {
            if (!banDao.chechTrungMa(txtMaBan.getText())) {
                MsgBox.alert(this, "Mã bàn đã tồn tại", JOptionPane.WARNING_MESSAGE);
            } else {
                Ban ban = getForm();
                try {
                    banDao.insert(ban);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Thêm mới thành công!", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    throw new RuntimeException(e);
//                    MsgBox.alert(this, "Thêm mới thất bại", JOptionPane.WARNING_MESSAGE);
                }
            }
        }

    }

    void update() {
        if (checkValidateForm()) {
            Ban ban = getForm();
            try {
                banDao.update(ban);
                this.fillTable();
                MsgBox.alert(this, "Cập nhật thành công!", JOptionPane.INFORMATION_MESSAGE);
                this.clearForm();
            } catch (Exception e) {
                //throw new RuntimeException(e);
                MsgBox.alert(this, "Cập nhật thất bại", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không có quyền xóa bàn!", JOptionPane.WARNING_MESSAGE);
        } else if (MsgBox.confirm(this, "Bạn thực sự muốn xóa bàn này?")) {
            String maKH = txtMaBan.getText();
            try {
                banDao.delete(maKH);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Xóa thành công", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                //throw new RuntimeException(e);
                MsgBox.alert(this, "Xóa thất bại", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    void clearForm() {
        Ban ban = new Ban();
        this.setForm(ban);
        this.row = -1;
        this.updateStatus();
        tblBan.clearSelection();

    }

    void edit() {
        String manv = (String) tblBan.getValueAt(this.row, 0);
        Ban ban = banDao.selectById(manv);
        this.setForm(ban);
        this.updateStatus();
    }
    void fillComboBoxKhuVuc(){
        List<KhuVuc> kv = kvdao.selectAll();
        cboChonKhuVuc.removeAllItems();
        for (KhuVuc khuVuc : kv) {
            cboChonKhuVuc.addItem(khuVuc.getTenKV());
        }
    }
    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblBan.getModel();
        model.setRowCount(0);
        try {
            String keyWord = txtTimKiem.getText();
            List<Ban> list = banDao.selectByKeyWord(keyWord);
            for (Ban ban : list) {
                KhuVuc kv = kvdao.selectById(ban.getKhuVuc());
                Object[] row = {ban.getMaBan(), ban.getTenBan(),kv.getTenKV(), ban.getTrangThai()};
                model.addRow(row);
            }
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    void setForm(Ban b) {
        txtMaBan.setText(b.getMaBan());
        txtTenban.setText(b.getTenBan());
        cboChonKhuVuc.setSelectedItem(b.getKhuVuc());
    }

    Ban getForm() {
        Ban ban = new Ban();
        ban.setMaBan(txtMaBan.getText());
        ban.setTenBan(txtTenban.getText());
        ban.setTrangThai("Trống");
        ban.setKhuVuc(cboChonKhuVuc.getSelectedItem() + "");

        return ban;
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        //Trạng thái form
        txtMaBan.setEditable(!edit);
        txtTenban.setEditable(!edit);
        cboChonKhuVuc.setEditable(!edit);

        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        btnThem.setText("Thêm");

    }

    boolean checkValidateForm() {
        if (txtMaBan.getText().isEmpty()) {
            MsgBox.alert(this, "Vui lòng nhập Mã Bàn!", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        String patternMaNV = "^B\\d+";
        if (!txtMaBan.getText().matches(patternMaNV)) {
            MsgBox.alert(this, "Sai mã bàn!\n Ví dụ:  B*** . \n* là các số", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if (txtTenban.getText().isEmpty()) {
            MsgBox.alert(this, "Vui lòng nhập tên bàn!", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if (!cboChonKhuVuc.isEnabled()) {
            MsgBox.alert(this, "Vui lòng chọn giới tính!", JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }

    private void timKiem() {
        this.fillTable();
        this.clearForm();
        this.row = -1;
        updateStatus();
    }

